// Aria unui dreptunghi: Lungime = a, Latime = b. Aria este a * b
// Perimetrul dreptunghiului este: 2 *( a + b )
// Diagonala dreptunghiului este a patrat + b patrat

function ariaDreptunghi(a, b){
    return a * b;
}

function perimetruDrept(a, b){
    return 2 * (a + b);
}

function diagonalaDrept(a, b){
    return a * a + b * b;
}